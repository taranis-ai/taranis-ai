<template>
  <div>
    <v-btn
      v-if="showImport"
      color="green-darken-3"
      dark
      class="ml-4"
      prepend-icon="mdi-file-import-outline"
      text="Import"
      @click="importClick"
    />
    <v-btn
      v-if="showExport"
      color="teal-darken-4"
      dark
      class="ml-4"
      prepend-icon="mdi-file-export-outline"
      text="Export"
      @click="exportFile"
    />
    <input
      ref="fileInput"
      :accept="accepts"
      type="file"
      hidden
      @change="fileSelected"
    />
  </div>
</template>

<script>
import { ref } from 'vue'

export default {
  name: 'ImportExport',
  props: {
    accepts: {
      type: String,
      default: 'application/json'
    },
    showImport: {
      type: Boolean,
      default: true
    },
    showExport: {
      type: Boolean,
      default: true
    }
  },
  emits: ['import', 'export'],
  setup(props, { emit }) {
    const fileInput = ref(null)

    const importClick = () => {
      fileInput.value.click()
    }

    const fileSelected = (event) => {
      const file = event.target.files[0]
      const formData = new FormData()
      formData.append('file', file)
      emit('import', formData)
    }

    const exportFile = () => {
      emit('export')
    }

    return {
      fileInput,
      importClick,
      fileSelected,
      exportFile
    }
  }
}
</script>

<style scoped>
button {
  display: inline-flex !important;
}
</style>
