<template>
  <DataTable
    :sort-by="sortBy"
    :items="report_items_data"
    :show-top="true"
    :header-filter="[
      'completed',
      'type',
      'title',
      'created',
      'stories',
      'actions'
    ]"
    :items-per-page="reportFilter.limit"
    @delete-item="deleteItem"
    @edit-item="editItem"
    @add-item="addItem"
    @update-items="updateData"
    @selection-change="selectionChange"
  >
    <template #actionColumn>
      <v-tooltip left>
        <template #activator="{ props }">
          <v-icon
            v-bind="props"
            color="secondary"
            @click.stop="createProduct(item)"
          >
            mdi-file
          </v-icon>
        </template>
        <span>Create Product</span>
      </v-tooltip>
    </template>
  </DataTable>
</template>

<script>
import DataTable from '@/components/common/DataTable.vue'
import {
  deleteReportItem,
  createReportItem,
  updateReportItem
} from '@/api/analyze'
import { notifySuccess, notifyFailure } from '@/utils/helpers'
import { useAnalyzeStore } from '@/stores/AnalyzeStore'
import { useFilterStore } from '@/stores/FilterStore'
import { useMainStore } from '@/stores/MainStore'
import { useRouter } from 'vue-router'
import { storeToRefs } from 'pinia'
import { onMounted, ref, computed } from 'vue'

export default {
  name: 'AnalyzeView',
  components: {
    DataTable
  },
  setup() {
    const sortBy = ref([{ key: 'created', order: 'desc' }])

    const mainStore = useMainStore()
    const analyzeStore = useAnalyzeStore()
    const filterStore = useFilterStore()
    const router = useRouter()
    const { reportFilter } = storeToRefs(filterStore)

    const { report_item_types, report_items } = storeToRefs(analyzeStore)
    const selected = ref([])

    const report_items_data = computed(
      () => analyzeStore.getReportItemsTableData
    )

    const updateData = () => {
      analyzeStore.loadReportItems().then(() => {
        mainStore.itemCountTotal = report_items.value.total_count
        mainStore.itemCountFiltered = report_items.value.items.length
      })
      analyzeStore.loadReportTypes()
    }

    const addItem = () => {
      router.push('/report/0')
    }

    const editItem = (item) => {
      router.push('/report/' + item.id)
    }

    const deleteItem = (item) => {
      deleteReportItem(item)
        .then((response) => {
          notifySuccess(response)
          updateData()
        })
        .catch((error) => {
          notifyFailure(error)
        })
    }

    const createItem = (item) => {
      createReportItem(item)
        .then((response) => {
          notifySuccess(response)
          updateData()
        })
        .catch((error) => {
          notifyFailure(error)
        })
    }

    const updateItem = (item) => {
      updateReportItem(item)
        .then((response) => {
          notifySuccess(response)
          updateData()
        })
        .catch((error) => {
          notifyFailure(error)
        })
    }

    const createProduct = () => {
      router.push({ name: 'product', params: { id: 0 } })
    }

    const selectionChange = (new_selection) => {
      selected.value = new_selection.map((item) => item.id)
    }

    onMounted(() => {
      updateData()
    })

    return {
      report_item_types,
      report_items_data,
      report_items,
      reportFilter,
      selected,
      sortBy,
      updateData,
      addItem,
      editItem,
      deleteItem,
      createItem,
      updateItem,
      createProduct,
      selectionChange
    }
  }
}
</script>
