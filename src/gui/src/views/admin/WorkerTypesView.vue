<template>
  <v-container fluid class="pa-2">
    <v-row no-gutters>
      <v-col class="pa-2 mt-2">
        <h1>Worker Types Settings</h1>
      </v-col>
    </v-row>
    <v-row no-gutters>
      <v-col class="pa-2">
        <DataTable
          v-model:items="worker_types.items"
          :add-button="false"
          :header-filter="[
            'name',
            'description',
            'category',
            'type',
            'actions'
          ]"
          sort-by-item="category"
          @edit-item="editItem"
          @update-items="updateData"
        >
        </DataTable>
        <EditConfig
          v-if="showForm"
          :config-data="formData"
          :form-format="formFormat"
          :parameters="disabledParameters"
          title="Worker Type Defaults"
          @submit="handleSubmit"
        />
      </v-col>
    </v-row>
  </v-container>
</template>

<script>
import DataTable from '@/components/common/DataTable.vue'
import EditConfig from '@/components/config/EditConfig.vue'
import { ref, computed, onMounted } from 'vue'
import { useConfigStore } from '@/stores/ConfigStore'
import { useMainStore } from '@/stores/MainStore'
import { notifyFailure, baseFormat, notifySuccess } from '@/utils/helpers'
import { storeToRefs } from 'pinia'
import { patchWorkerType } from '@/api/config'

export default {
  name: 'WorkerTypesView',
  components: {
    DataTable,
    EditConfig
  },
  setup() {
    const mainStore = useMainStore()
    const configStore = useConfigStore()

    // data
    const { worker_types, parameters } = storeToRefs(configStore)
    const formData = ref({})
    const showForm = ref(false)

    // computed
    const formFormat = computed(() => {
      const additionalFormat = [
        {
          name: 'type',
          label: 'Type',
          type: 'text',
          disabled: true
        }
      ]
      return [...baseFormat, ...additionalFormat]
    })

    const disabledParameters = computed(() => {
      const result = {}
      for (const key in parameters.value) {
        result[key] = parameters.value[key].map((param) => {
          return {
            ...param
          }
        })
      }
      return result
    })

    // methods
    function updateData() {
      configStore.loadWorkerTypes().then(() => {
        mainStore.itemCountTotal = worker_types.value.total_count
        mainStore.itemCountFiltered = worker_types.value.items.length
      })
      configStore.loadParameters()
    }

    function editItem(item) {
      formData.value = item
      showForm.value = true
    }

    function handleSubmit(submittedData) {
      patchWorkerType(submittedData)
        .then(() => {
          notifySuccess('Worker Type updated')
          showForm.value = false
          updateData()
        })
        .catch((error) => {
          notifyFailure(error)
        })
    }

    onMounted(() => {
      updateData()
    })

    return {
      // data
      worker_types,
      formData,
      showForm,

      // computed
      formFormat,
      disabledParameters,

      // methods
      updateData,
      editItem,
      handleSubmit
    }
  }
}
</script>
